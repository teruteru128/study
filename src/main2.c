/**
 * @brief
 * ↓2回連続getFloatで-1が出るseed 2つ
 * 125352706827826
 * 116229385253865
 * ↓getDoubleで可能な限り1に近い値が出るseed
 * 155239116123415
 * preforkする場合ってforkするのはlistenソケットを開く前？開いた後？
 * ハッシュの各バイトを１バイトにORで集約して結果が0xffにならなかったら成功
 * 丸数字の1から50までforで出す
 * timer_create+sigeventでタイマーを使って呼ばれたスレッドから新しくスレッドを起動する
 *
 * decodable random source?
 *
 * @param argc
 * @param argv
 * @param envp
 * @return int
 */
int hiho(int argc, char **argv, const char *const *envp)
{
    pid_t pid = fork();
    if (pid < 0)
    {
        perror("fork");
        exit(255);
    }
    else if (pid == 0)
    {
        sleep(1);
        printf("うんちー！\n");
        return 12;
    }
    // 親プロセス
    printf("parent process start\n");

    int status;
    pid_t r = waitpid(pid, &status,
                      0); // 子プロセスのプロセスIDを指定して、終了を待つ
    if (r < 0)
    {
        perror("waitpid");
        exit(-1);
    }
    printf("%04x\n", status);
    if (WIFEXITED(status))
    {
        // 子プロセスが正常終了の場合
        int exit_code = WEXITSTATUS(status); // 子プロセスの終了コード
        printf("child exit-code=%d\n", exit_code);
    }
    else
    {
        printf("child status=%04x\n", status);
    }

    printf("parent process end\n");
    return 0;
}
